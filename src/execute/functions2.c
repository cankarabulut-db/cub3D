#include "../cub3d.h"

t_vec2	g_vec2_null(void)
{
	return ((t_vec2){.x = 0, .y = 0});
}

t_vec2	g_vec2_i(void)
{
	return ((t_vec2){.x = 1, .y = 0});
}

t_vec2	g_vec2_j(void)
{
	return ((t_vec2){.x = 0, .y = 1});
}
